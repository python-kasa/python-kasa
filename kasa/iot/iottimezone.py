"""Module for io device timezone lookups."""

from __future__ import annotations

import logging
import re
from datetime import datetime, tzinfo
from typing import TYPE_CHECKING, cast

from zoneinfo import ZoneInfo

_LOGGER = logging.getLogger(__name__)


def get_timezone(index: int) -> tzinfo:
    """Get the timezone from the index."""
    if index > 109:
        _LOGGER.error(
            "Unexpected index %s not configured as a timezone, defaulting to UTC", index
        )
        return ZoneInfo("Etc/UTC")

    id_index = None
    index_name = None
    for key, values in TIMEZONE_INDEX.items():
        if values["index"] == index:
            index_name = key
            break
    if TYPE_CHECKING:
        # We know index is in the dict if it's <= 109
        assert id_index

    possibles = []
    for key, values in TIMEZONE_ID.items():
        if values["index"] == index_name:
            possibles.append(key)
            if values.get("is_main", False):
                possibles = [key]
                break
    if len(possibles) == 0:  # pragma: no cover
        _LOGGER.error(
            "Could not find index_name %s in timezone id list, defaulting to UTC",
            index_name,
        )
        return ZoneInfo("Etc/UTC")
    if len(possibles) == 1:
        return ZoneInfo(possibles[0])
    for name in re.sub(r"\(.*?\)", "", index_name).strip().split(", "):
        for possible in possibles:
            if name in possible:
                return ZoneInfo(possible)
    return ZoneInfo(possibles[0])


def get_timezone_index(name: str) -> int:
    """Return the iot firmware index for a valid IANA timezone key."""
    if not (index_item := TIMEZONE_ID.get(name)):
        # Try to find a supported timezone matching dst true/false
        zone = ZoneInfo(name)
        now = datetime.now()
        winter = datetime(now.year, 1, 1, 12)
        summer = datetime(now.year, 7, 1, 12)
        for i in range(110):
            configured_zone = get_timezone(i)
            if zone.utcoffset(winter) == configured_zone.utcoffset(
                winter
            ) and zone.utcoffset(summer) == configured_zone.utcoffset(summer):
                return i
        raise ValueError("Device does not support timezone %s", name)

    return cast(int, TIMEZONE_INDEX[cast(dict, index_item)["index"]]["index"])


